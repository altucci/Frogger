    @Override
    public void paintComponent(Graphics g) 
    {
        super.paintComponent(g);
        
        g.setFont(new java.awt.Font("OCR A Extended", 1, 24));
        g.setColor(Color.YELLOW);
        g.drawString("Time:   " + time, 360, 40);
        
        g.setFont(new java.awt.Font("OCR A Extended", 1, 24));
        g.setColor(Color.RED);
        g.drawString("Score:   " + score, 528, 40);
        
        for (int i = 0;i < 4;i++)
        {
            if (lives[i] == true)
            {
                g.drawImage(new ImageConstants().frogstillup, 96 + (i*48), 9, this);
            }
        }
    }
    
    public void start()
    {
        if (application == null)
        {
            application = new Thread();
            application.start();
        }
    }
    
    @Override
    public void run()
    {
        while (true)
        {
            repaint();
        }
    }
    
