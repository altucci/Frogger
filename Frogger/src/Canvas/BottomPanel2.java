    public void resetLives()
    {
        for (int i = 0;i < 4;i++)
        {
            lives[i] = true;
        }
    }
    
    public void lifeLost()
    {
        boolean more = true;
        
        for (int i = 3;i >= 0 && more;i--)
        {
            if (lives[i] == true)
            {
                lives[i] = false;
                
                more = false;
            }
        }
    }
    
